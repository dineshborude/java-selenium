public class DataTypes {

    public static void main(String[] args) {

        int i = 1234; //Stores integer values
        float f = 1.23f;
        long l = 1231313131313l;
        double d = 1.2356;
        boolean b = true;
        String s = "String";

        /* Naming Conventions :
        Classes : First Letter should be capital and then camel case. It should be noun. Ex : Dog, Car, MyCar.
        Interface : First Letter should be capital and then camel case. It should be Adjective. Ex : Runnable, Interested.
        Method: first letter should be small and then camel case. It should be verb-noun pair, Ex: getCar, getName.
        Constant: All in upercase and underscore between words. Ex : CONSTANT_VARIABLE, PI.
         */

        System.out.println(s);
    }

}